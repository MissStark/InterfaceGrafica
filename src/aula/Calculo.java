package aula;

public class Calculo {
	public double soma(double v1, double v2){
		double total;
		total = v1 + v2;
		return total;
	}
	public double sub(double v1, double v2){
		double total;
		total = v1 - v2;
		return total;
	}
	public double mult(double v1, double v2){
		double total;
		total = v1 * v2;
		return total;
	}
	public double div(double v1, double v2){
		double total;
		total = v1 / v2;
		return total;
	}
	
}
